            foreach ($imageFields as $field => $imagePath) {
                if (!empty($imagePath)) {
                    $userDetail->images()->create([
                        'url'            => $imagePath,
                        'imageable_type' => UserDetail::class,
                        'imageable_id'   => $userDetail->id,
                        'type'           => $field,
                        'created_at'     => now(),
                        'updated_at'     => now(),
                    ]);
                }
            }
            $roleName = $row['role'] ?? 'user';
            $userRole = Role::firstOrCreate(['name' => $roleName]);
            $user->assignRole($userRole);
        }
    }

    private function getSettingIdByName($name)
    {
        return Setting::where('key', $name)->first()->id;
    }
